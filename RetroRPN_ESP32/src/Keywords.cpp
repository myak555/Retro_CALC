Keyword *Keywords::findByName(byte *str){
  for(size_t i=0; i<_nKey; i++){
    lastKeywordFound  = _keywords + i;
    if( lastKeywordFound->name0 && IsKeyword(str, lastKeywordFound->name0)) return lastKeywordFound;
    if( lastKeywordFound->name1 && IsKeyword(str, lastKeywordFound->name1)) return lastKeywordFound;
  }
  lastKeywordFound = NULL;
  return NULL; // not found
}

Keyword *Keywords::findById( int16_t id){
  for(size_t i=0; i<_nKey; i++){
    lastKeywordFound  = _keywords + i;
    if( id == lastKeywordFound->id) return lastKeywordFound;
  }
  lastKeywordFound = NULL;
  return NULL; // not found
}

bool Keywords::isKeyword(byte *str){
  Keyword *tmp = _keywords;
  for(size_t i=0; i<_nKey; i++, tmp++){
    if( tmp->name0 && IsKeyword(str, tmp->name0)) return true;
    if( tmp->name0 && IsKeyword(str, tmp->name1)) return true;
  }
  return false; // not found
}

byte *Keywords::parse( byte *str){
  lastKeywordFound = NULL;
  byte tmp;
  byte *ptr = str;

  // skip empty space
  while( IsSpacer(*ptr)) ptr++;
  if( !IsNameStarter(*ptr)) return ptr;
  str = ptr;

  while( IsNameCharacter(*ptr)){
    // dollar or percent must be the last character
    if( *ptr == _DOLLAR_ || *ptr == _PERCENT_){
      ptr++;
      break;
    }
    ptr++;
  }
  tmp = *ptr; // preserve symbol
  *ptr = _NUL_;
  if( findByName(str) != NULL) str = ptr;
  *ptr = tmp; // restore symbol
  return str;
}

void Keywords::_addOperator( const char *name1, const char *name2, int16_t id, void *method){
  if( id >= _OPERATOR_COUNT) return;
  Keyword *tmp = _keywords + _nKey;
  if( (byte *)tmp >= _namestack) return;
  _nKey++;
  tmp->id = id;
  tmp->keywordType = _KEYTYPE_OPERATOR;
  tmp->nArgs = _KEYWORD_ONLY_;
  tmp->name0 = name2; // small letters will be checked first
  tmp->name1 = name1;
  tmp->operator_ptr = method;
  #ifdef __DEBUG
  Serial.print( _nKey);
  Serial.print(" added operator keyword ");
  Serial.print( tmp->name0);
  Serial.print(", AKA ");
  Serial.print( tmp->name1);
  Serial.println( ".");
  #endif
}

void Keywords::_addFunction( const char *name1, const char *name2, int16_t id, int8_t nArgs, void *method){
  if( id >= _OPERATOR_COUNT) return;
  Keyword *tmp = _keywords + _nKey;
  if( (byte *)tmp >= _namestack) return;
  _nKey++;
  tmp->id = id;
  tmp->keywordType = _KEYTYPE_FUNCTION;
  tmp->nArgs = nArgs;
  tmp->name0 = name2; // small letters will be checked first
  tmp->name1 = name1;
  tmp->operator_ptr = method;
  #ifdef __DEBUG
  Serial.print( _nKey);
  Serial.print(" added function keyword ");
  Serial.print( tmp->name0);
  Serial.print(", AKA ");
  Serial.print( tmp->name1);
  Serial.println( ".");
  #endif
}
